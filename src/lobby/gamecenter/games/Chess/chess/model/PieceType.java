package lobby.gamecenter.games.Chess.chess.model;

/**
 * Represents the types of chess pieces. Each piece type corresponds to a
 * standard chess piece.
 *
 * @author Do Duc Hai - CE192014
 */
public enum PieceType {

    /**
     * The King piece.
     */
    KING,
    /**
     * The Queen piece.
     */
    QUEEN,
    /**
     * The Rook piece.
     */
    ROOK,
    /**
     * The Bishop piece.
     */
    BISHOP,
    /**
     * The Knight piece.
     */
    KNIGHT,
    /**
     * The Pawn piece.
     */
    PAWN
}
